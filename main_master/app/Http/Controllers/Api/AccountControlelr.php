    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if (!$this->isAuthorized) {
            return prepareResult(false, [], [], "User not authenticate", $this->unauthorized);
        }

        $account = Accounts::select('id', 'uuid', 'account_name')
            ->get();
        $account_array = array();
        if (is_object($account)) {
            foreach ($account as $key => $account1) {
                $account_array[] = $account[$key];
            }
        }

        $data_array = array();
        $page = (isset($_REQUEST['page'])) ? $_REQUEST['page'] : '';
        $limit = (isset($_REQUEST['page_size'])) ? $_REQUEST['page_size'] : '';
        $pagination = array();
        if ($page != '' && $limit != '') {
            $offset = ($page - 1) * $limit;
            for ($i = 0; $i < $limit; $i++) {
                if (isset($account_array[$offset])) {
                    $data_array[] = $account_array[$offset];
                }
                $offset++;
            }

            $pagination['total_pages'] = ceil(count($account_array) / $limit);
            $pagination['current_page'] = (int)$page;
            $pagination['total_records'] = count($account_array);
        } else {
            $data_array = $account_array;
        }

        return prepareResult(true, $data_array, [], "Account listing", $this->success, $pagination);
    }

